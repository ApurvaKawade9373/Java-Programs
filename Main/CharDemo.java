
public class CharDemo {

	public static void main(String[] args) {
		// assign single character literal
		 char ch='a';
		 System.out.println(ch);
		 
		 // assigning numbers to the char
		 char ch1=65;                //number is converted into character, because char doesn't support number
		 System.out.println(ch1);     // 65 is an ASCII Value of A So, it gives ans 'A'
		 
		 //assigning unicode
		 char var1='\u00A7';
		 System.out.println(var1);
		 
		 //ASCII Code representation
		 int a='A';
		 System.out.println(a);

	}

}
